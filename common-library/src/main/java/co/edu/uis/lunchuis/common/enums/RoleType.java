package co.edu.uis.lunchuis.common.enums;

/**
 * Represents the types of roles available in the system.
 * The {@code RoleType} enumeration is used to define high-level categories
 * or permissions for users within the system. Each role encapsulates specific
 * responsibilities and access privileges that a user may have.
 * Enum Constants:
 * - STUDENT: Represents a user role of a student, typically with limited access permissions.
 * - ADMIN: Represents a user role of an administrator, typically with elevated access permissions.
 */
public enum RoleType {
    STUDENT,
    ADMIN
}
